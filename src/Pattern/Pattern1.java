package Pattern;

public class Pattern1 {
    

    public static void main(String[] args) {

        int i,j;
        for(i=1;i<=4;i++) //outer loop
        {
            for(j=1;j<=9;j++){  //inner loop

                System.out.print(j);

            }
            System.out.println();
        }
    }
}
